        public static string GetNote(int index, int shift = 0)
        {
            var shiftedIndex = index + (shift * 12);

            if (shiftedIndex >= 0 && shiftedIndex < Notes.Count)
                return Notes[shiftedIndex];

            return NotePause;
        }

        public static int GetNoteValue(string note)
        {
            return NotesValues.TryGetValue(note, out int value) ? value : 0;
        }

